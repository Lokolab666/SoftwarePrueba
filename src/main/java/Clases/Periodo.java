package Clases;

public class Periodo {

	private int id_Periodo;
	private String periodo;
	private int cantidad_Matriculados;
	
	public int getId_Periodo() {
		return id_Periodo;
	}
	public void setId_Periodo(int id_Periodo) {
		this.id_Periodo = id_Periodo;
	}
	public String getPeriodo() {
		return periodo;
	}
	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}
	public int getCantidad_Matriculados() {
		return cantidad_Matriculados;
	}
	public void setCantidad_Matriculados(int cantidad_Matriculados) {
		this.cantidad_Matriculados = cantidad_Matriculados;
	}
	
	
}
